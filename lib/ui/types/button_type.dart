enum XButtonType {
  /// Default type is [XButtonType.primary], used to fill with color for widget
  primary,

  /// Type [XButtonType.outline], used for widget with outline border and fill color with Colors.transparent
  outline,

  /// Type [XButtonType.outline2x], used for widget with outline2x border and border.width = 2.0 and fill color with Colors.transparent
  secondary,
}